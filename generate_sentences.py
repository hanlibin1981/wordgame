def get_sentence(word, meaning):
    """Get sentence and translation based on word or meaning."""
    word_lower = word.lower().strip()

    # Try exact word match
    if word_lower in SENTENCE_TEMPLATES:
        return SENTENCE_TEMPLATES[word_lower]

    # Try meaning keywords
    for key, value in SENTENCE_TEMPLATES.items():
        if key in meaning or meaning in key:
            return value

    # Try part of speech patterns
    pos_match = re.match(r'^(n\.|v\.|adj\.|adv\.|art\.|pron\.|conj\.|prep\.)', meaning.strip())
    if pos_match:
        pos = pos_match.group(1)
        # Generate based on part of speech
        if pos == 'n.':
            return (f"The {word} is important.", f"{word}很重要。")
        elif pos == 'v.':
            return (f"We must {word} carefully.", f"我们必须谨慎地{get_chinese_verb(word)}。")
        elif pos == 'adj.':
            return (f"This is {word}.", f"这是{get_chinese_adj(word, meaning)}。")

    # Default fallback
    return (f"The word '{word}' is commonly used.", f"'{word}'是一个常用词。")

