def main():
    # Load the vocabulary
    with open('/Users/mac/openclaw-projects/wordgame/WordGame/Resources/Vocabularies/high_school_3500.json', 'r', encoding='utf-8') as f:
        data = json.load(f)

    words = data['words']
    updated_count = 0
    template_count = 0

    for word_entry in words:
        # Skip if already has a sentence
        if word_entry.get('sentence'):
            continue

        word = word_entry.get('word', '')
        meaning = word_entry.get('meaning', '')

        # Generate sentence
        sentence, translation = get_sentence(word, meaning)
        word_entry['sentence'] = sentence
        word_entry['sentenceTranslation'] = translation
        updated_count += 1
        if word.lower() in SENTENCE_TEMPLATES:
            template_count += 1

    print(f'Updated {updated_count} words with example sentences.')
    print(f'Used template matches: {template_count}')

    # Save the updated vocabulary
    output_path = '/Users/mac/openclaw-projects/wordgame/WordGame/Resources/Vocabularies/high_school_3500.json'
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f'Saved to {output_path}')

if __name__ == '__main__':
    main()
